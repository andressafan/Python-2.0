# Operadores Aritméticos
# (+) : Adição
# (-) : Subtração
# (*) : Multiplicação
# (/) : Divisão
# (**) : Exponenciação
# (//) : Divisão inteira
# (%) : Resto da divisão
# (==) : Igual à

# Operandos são os números, ‘strings’, ou variáveis
# 5 + 2 ==  7
# 5 - 2 == 3
# 5 * 2 == 10
# 5 / 2 == 2.5
# 5 ** 2 == 25
# 5 // 2 == 2
# 5 % 2 == 1

# Ordem de precedência
# 1° : ()
# 2° : **
# 3° : *, /, //, %
# 4° : +, -

