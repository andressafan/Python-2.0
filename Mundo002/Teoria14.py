# Estrutura de repetição while

# __ __ __ __ __ __ __ __ _🍎_
# __ __ __ __ __ __ __ __ __ __ __ __ __ ... _🍎_

# se você não souber o limite final, você pode usar a estrutura while
# com uma condição

# enquanto (while) não chegar na maçã dê um passo

# enquanto não 🍎 (enquanto essa condição for verdadeira ele executa o comando)
#   passo (comando interno ao laço)
# pega (quando a condição for false irá executar o comando fora do laço)

# Em Python:

# while not 🍎:
#   passo
# pega


# ____  _💰_  _____💰_  _💰__🍎_

# enquanto não chegar na maçã dê passo
#   se (V) tiver chão na frente dê passo
#   se (V) tiver buraco pule
#   se (V) tiver dinheiro pegue
# se chegou na maça pegue e pare (saindo do laço)


# enquanto não 🍎:                       while not 🍎:
#   se chão                                 if chão:
#       passo                                  passo
#   se buraco                               if buraco:
#       pula                                   pula
#   se 💰                                   if 💰:
#       pega                                   pega
# pega 🍎                                pega 🍎




